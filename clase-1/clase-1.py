#  -- Hab. comunicativas e interpersonales
#  -- Capacidad lógica para resolución de problemas
#  -- Conocimientos en sistemas de seguridad para datos
#  -- Conocimientos básicos en matemáticas y estadísticas
#
#  - OLTP -> Online Transaction Processing. Diseñado para el procesamiento eficiente de transacciones diarias en tiempo real. Gestiona operaciones de rutina como inserciones, actualizaciones y eliminaciones en una base de datos.
#  - OLAP -> Online Analytical Processing. Se utiliza para el análisis de datos y la generación de informes. Proporciona a los usuarios una visión multidimensional de los datos para facilitar la toma de decisiones estratégicas.
