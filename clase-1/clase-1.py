#  - Las 3 V de Big Data
#  -- Volumen -> grandes cantidades de información
#  -- Velocidad -> grandes velocidades de información
#  -- Variedad -> Estructurada, Semiestructurada, No estructurada
#  -- Se pueden considerar también Veracidad y Valor
